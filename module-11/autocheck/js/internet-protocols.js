/*HyperText Transfer Protocol Secure — це налаштування 
над протоколом HTTP, у якому всі повідомлення між 
клієнтом і сервером шифруються з метою підвищення безпеки.

HTTPS протокол забезпечує захист від атак, що базуються 
на “прослуховуванні” з'єднання. Дані передаються з 
використанням криптографічних протоколів SSL або TLS.

Під час обміну через звичайне HTTP-з'єднання всі дані 
передаються у вигляді тексту і можуть бути прочитані 
всіма, хто отримав доступ до з'єднання між клієнтом і 
сервером. Якщо користувачі роблять покупки онлайн і 
заповнюють форму замовлення, що містить інформацію 
про кредитну картку, їхні фінансові дані набагато 
легше викрасти, якщо вони передаються у вигляді тексту. 
З HTTPS дані будуть зашифровані і хакер не зможе їх 
розшифрувати, тому що для розшифрування необхідний 
доступ до закритого ключа, який зберігається на сервері.

Протокол HTTPS гарантує, що інформація про клієнта, 
наприклад номери кредитних карток, зашифрована і не 
може бути перехоплена в розшифрованому вигляді. 
Відвідувачі можуть переконатися, що сайт безпечний, 
подивившись на іконку зліва від адресного рядка, 
захищені з'єднання позначаються іконкою замка.

Яке з наступних тверджень правильно описує основну 
різницю між HTTP і HTTPS?

HTTPS більш безпечний за HTTP. Він шифрує дані, 
передані між браузером і сервером.*/ 
  